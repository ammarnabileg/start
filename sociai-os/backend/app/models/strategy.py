# ─── Business Goal Model ──────────────────────────────────────────────────────

class BusinessGoal(Base):
    """
    SMART business goals tied to a marketing strategy with KPI tracking.
    """
    __tablename__ = "business_goals"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=uuid.uuid4
    )
    strategy_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        ForeignKey("marketing_strategies.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )
    name: Mapped[str] = mapped_column(String(255), nullable=False)
    description: Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    goal_type: Mapped[GoalType] = mapped_column(Enum(GoalType), nullable=False, index=True)
    status: Mapped[GoalStatus] = mapped_column(
        Enum(GoalStatus), default=GoalStatus.NOT_STARTED, nullable=False
    )

    # KPI definition
    metric_name: Mapped[str] = mapped_column(String(100), nullable=False)
    metric_unit: Mapped[str] = mapped_column(String(50), nullable=False)  # e.g., "followers", "%", "$"
    baseline_value: Mapped[Optional[float]] = mapped_column(Float, nullable=True)
    target_value: Mapped[float] = mapped_column(Float, nullable=False)
    current_value: Mapped[float] = mapped_column(Float, default=0.0, nullable=False)

    # Timeline
    due_date: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True), nullable=True)
    achieved_at: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True), nullable=True)

    # Progress tracking
    progress_percentage: Mapped[float] = mapped_column(Float, default=0.0, nullable=False)
    milestones: Mapped[Optional[list]] = mapped_column(JSONB, nullable=True, default=list)
    progress_history: Mapped[Optional[list]] = mapped_column(JSONB, nullable=True, default=list)

    sort_order: Mapped[int] = mapped_column(Integer, default=0, nullable=False)
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), server_default=func.now(), nullable=False
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), server_default=func.now(), onupdate=func.now(), nullable=False
    )

    # Relationships
    strategy: Mapped["MarketingStrategy"] = relationship(
        "MarketingStrategy", back_populates="business_goals"
    )

    @property
    def progress(self) -> float:
        """Calculate progress % toward goal. Returns value between 0 and 100."""
        if not self.baseline_value:
            baseline = 0.0
        else:
            baseline = self.baseline_value
        total_required = self.target_value - baseline
        if total_required <= 0:
            return 100.0
        achieved = self.current_value - baseline
        return min(100.0, max(0.0, (achieved / total_required) * 100))


