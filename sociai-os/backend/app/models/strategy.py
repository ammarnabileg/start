# ─── Marketing Strategy Model ─────────────────────────────────────────────────

class MarketingStrategy(Base):
    """
    Top-level marketing strategy for a user's brand / business.
    Generated by AI agents or created manually.
    """
    __tablename__ = "marketing_strategies"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), primary_key=True, default=uuid.uuid4
    )
    user_id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True), ForeignKey("users.id", ondelete="CASCADE"), nullable=False, index=True
    )
    name: Mapped[str] = mapped_column(String(255), nullable=False)
    description: Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    status: Mapped[StrategyStatus] = mapped_column(
        Enum(StrategyStatus), default=StrategyStatus.DRAFT, nullable=False, index=True
    )

    # Strategy context (AI-parsed brand voice, positioning, etc.)
    brand_voice: Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    unique_value_proposition: Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    competitive_advantages: Mapped[Optional[list]] = mapped_column(JSONB, nullable=True, default=list)
    target_platforms: Mapped[Optional[list]] = mapped_column(JSONB, nullable=True, default=list)
    posting_frequency: Mapped[Optional[dict]] = mapped_column(JSONB, nullable=True, default=dict)
    optimal_posting_times: Mapped[Optional[dict]] = mapped_column(JSONB, nullable=True, default=dict)
    content_mix: Mapped[Optional[dict]] = mapped_column(JSONB, nullable=True, default=dict)

    # AI generation metadata
    ai_generated: Mapped[bool] = mapped_column(Boolean, default=False, nullable=False)
    ai_model_used: Mapped[Optional[str]] = mapped_column(String(100), nullable=True)
    generation_prompt: Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    generation_metadata: Mapped[Optional[dict]] = mapped_column(JSONB, nullable=True)

    # Strategy period
    start_date: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True), nullable=True)
    end_date: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True), nullable=True)

    # Soft delete
    deleted_at: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True), nullable=True)
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), server_default=func.now(), nullable=False
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), server_default=func.now(), onupdate=func.now(), nullable=False
    )

    # Relationships
    owner: Mapped["User"] = relationship("User", back_populates="strategies")
    brand_guidelines: Mapped[List["BrandGuideline"]] = relationship(
        "BrandGuideline", back_populates="strategy", cascade="all, delete-orphan"
    )
    content_plans: Mapped[List["ContentPlan"]] = relationship(
        "ContentPlan", back_populates="strategy", cascade="all, delete-orphan"
    )
    target_audiences: Mapped[List["TargetAudience"]] = relationship(
        "TargetAudience", back_populates="strategy", cascade="all, delete-orphan"
    )
    business_goals: Mapped[List["BusinessGoal"]] = relationship(
        "BusinessGoal", back_populates="strategy", cascade="all, delete-orphan"
    )
    competitor_refs: Mapped[List["CompetitorRef"]] = relationship(
        "CompetitorRef", back_populates="strategy", cascade="all, delete-orphan"
    )

    __table_args__ = (
        Index("ix_marketing_strategies_user_status", "user_id", "status"),
    )


