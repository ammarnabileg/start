        ['Interviews: View',       'interviews.view',   'interviews'],
        ['Interviews: Schedule',   'interviews.schedule','interviews'],
        ['Interviews: Conduct',    'interviews.conduct','interviews'],
        ['Interviews: Review',     'interviews.review', 'interviews'],
        // Applications
        ['Applications: View',     'applications.view', 'applications'],
        ['Applications: Move',     'applications.move', 'applications'],
        ['Applications: Reject',   'applications.reject','applications'],
        // Reports
        ['Reports: View',          'reports.view',      'reports'],
        ['Reports: Export',        'reports.export',    'reports'],
