    );
    foreach ($permissions as $p) {
        $permStmt->execute($p);
    }

    // ── Assign all permissions to super-admin role ─────────────────────────────
    $superRole = $pdo->query("SELECT id FROM `roles` WHERE `slug` = 'super-admin' LIMIT 1")->fetch();
    if ($superRole) {
        $allPerms = $pdo->query("SELECT id FROM `permissions`")->fetchAll(PDO::FETCH_COLUMN);
        $rpStmt   = $pdo->prepare("INSERT IGNORE INTO `role_permissions` (`role_id`, `permission_id`) VALUES (?, ?)");
        foreach ($allPerms as $pid) {
            $rpStmt->execute([$superRole['id'], $pid]);
        }
    }

    // ── Assign relevant permissions to admin role ──────────────────────────────
    $adminRole = $pdo->query("SELECT id FROM `roles` WHERE `slug` = 'admin' LIMIT 1")->fetch();
