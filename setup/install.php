    );
    foreach ($roles as $r) {
        $roleStmt->execute([$r['name'], $r['slug'], $r['description'], $r['is_system']]);
    }

    // ── Permissions ────────────────────────────────────────────────────────────
    $permissions = [
        // Jobs
        ['Jobs: View All',         'jobs.view',         'jobs'],
