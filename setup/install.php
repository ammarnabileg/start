// ─────────────────────────────────────────────────────────────────────────────
// createSuperAdmin
// Creates the super admin user record.
// Returns the new user ID.
// ─────────────────────────────────────────────────────────────────────────────
function createSuperAdmin(PDO $pdo, array $data): int
{
    $passwordHash = password_hash($data['password'], PASSWORD_BCRYPT, ['cost' => 12]);

    // Check for existing user
    $existing = $pdo->prepare("SELECT id FROM `users` WHERE `email` = ? LIMIT 1");
    $existing->execute([$data['email']]);
    $row = $existing->fetch();
    if ($row) {
        // Update existing user to super admin
        $pdo->prepare(
            "UPDATE `users` SET
                `first_name`           = ?,
                `last_name`            = ?,
                `password_hash`        = ?,
                `is_super_admin`       = 1,
                `status`               = 'active',
                `email_verified_at`    = NOW(),
                `onboarding_completed` = 0,
                `tenant_id`            = ?
             WHERE `email` = ?"
        )->execute([$data['first_name'], $data['last_name'], $passwordHash, $data['tenant_id'], $data['email']]);
        return (int)$row['id'];
    }

    $stmt = $pdo->prepare(
        "INSERT INTO `users`
            (`tenant_id`, `first_name`, `last_name`, `email`, `password_hash`,
             `is_super_admin`, `status`, `email_verified_at`, `onboarding_completed`)
         VALUES
            (?, ?, ?, ?, ?, 1, 'active', NOW(), 0)"
    );
    $stmt->execute([
        $data['tenant_id'],
        $data['first_name'],
        $data['last_name'],
        $data['email'],
        $passwordHash,
    ]);

    $newId = (int)$pdo->lastInsertId();
    if ($newId === 0) {
        throw new RuntimeException('Failed to retrieve new user ID after insert.');
    }
    return $newId;
}

// ─────────────────────────────────────────────────────────────────────────────
// createDefaultTenant
// Inserts the first company/tenant record.
// Returns the new tenant ID.
// ─────────────────────────────────────────────────────────────────────────────
function createDefaultTenant(PDO $pdo, array $data): int
{
    // Check for existing slug
    $existing = $pdo->prepare("SELECT id FROM `tenants` WHERE `slug` = ? LIMIT 1");
    $existing->execute([$data['slug']]);
    $row = $existing->fetch();
    if ($row) {
        // Update existing tenant
        $pdo->prepare(
            "UPDATE `tenants` SET `name` = ?, `domain` = ?, `status` = 'active', `plan` = 'enterprise' WHERE `id` = ?"
        )->execute([$data['name'], $data['domain'], $row['id']]);
        return (int)$row['id'];
    }

    $stmt = $pdo->prepare(
        "INSERT INTO `tenants` (`name`, `slug`, `domain`, `status`, `plan`)
         VALUES (?, ?, ?, 'active', 'enterprise')"
    );
    $stmt->execute([$data['name'], $data['slug'], $data['domain']]);

    $newId = (int)$pdo->lastInsertId();
    if ($newId === 0) {
        throw new RuntimeException('Failed to retrieve new tenant ID after insert.');
    }

    // Seed AI settings for the tenant
    try {
        $pdo->prepare(
            "INSERT IGNORE INTO `tenant_ai_settings`
                (`tenant_id`, `enable_video_interviews`, `enable_voice_interviews`, `enable_text_interviews`)
             VALUES (?, 1, 1, 1)"
        )->execute([$newId]);
    } catch (Throwable) {
        // Non-fatal: table may not exist yet
    }

    return $newId;
}
