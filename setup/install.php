// ─────────────────────────────────────────────────────────────────────────────
// createEnvFile
// Writes a .env file to the project root with all required variables.
// ─────────────────────────────────────────────────────────────────────────────
function createEnvFile(string $rootDir, array $data): void
{
    $jwtSecret  = bin2hex(random_bytes(32));   // 64 hex chars
    $appKey     = 'base64:' . base64_encode(random_bytes(32));
    $appName    = $data['app_name'] ?? 'AI Recruit';
    $appUrl     = $data['app_url']  ?? 'http://localhost';
    $dbHost     = $data['db_host']  ?? '127.0.0.1';
    $dbPort     = $data['db_port']  ?? '3306';
    $dbName     = $data['db_name']  ?? 'ai_recruitment';
    $dbUser     = $data['db_user']  ?? 'root';
    $dbPass     = $data['db_pass']  ?? '';

    // Escape values containing spaces or special chars for .env
    $escape = static function (string $val): string {
        if (preg_match('/[\s"\'\\\\#]/', $val)) {
            return '"' . addslashes($val) . '"';
        }
        return $val;
    };

    $env = <<<EOT
# ============================================================
# AI Recruitment SaaS Platform — Environment Configuration
# Generated by Setup Wizard on {$_SERVER['REQUEST_TIME_FLOAT']} UTC
# ============================================================

# Application
APP_NAME={$escape($appName)}
APP_URL={$escape($appUrl)}
APP_ENV=production
APP_KEY={$appKey}
APP_DEBUG=false

# Database
DB_HOST={$escape($dbHost)}
DB_PORT={$dbPort}
DB_NAME={$escape($dbName)}
DB_USERNAME={$escape($dbUser)}
DB_PASSWORD={$escape($dbPass)}

# Authentication (JWT)
JWT_SECRET={$jwtSecret}
JWT_EXPIRY=86400

# Mail
MAIL_DRIVER=smtp
MAIL_HOST=smtp.mailtrap.io
MAIL_PORT=587
MAIL_USERNAME=
MAIL_PASSWORD=
MAIL_FROM_ADDRESS=noreply@example.com
MAIL_FROM_NAME={$escape($appName)}
MAIL_ENCRYPTION=tls

# File uploads
UPLOAD_MAX_SIZE=10240
UPLOAD_ALLOWED_TYPES=pdf,doc,docx,jpg,jpeg,png,gif,mp4,webm

# Session
SESSION_LIFETIME=120

EOT;

    $path = $rootDir . '/.env';
    if (file_put_contents($path, $env) === false) {
        throw new RuntimeException("Cannot write to {$path}. Check directory permissions.");
    }
    chmod($path, 0600);
}

// ─────────────────────────────────────────────────────────────────────────────
// runSchema
// Reads /database/schema.sql and executes each statement.
// ─────────────────────────────────────────────────────────────────────────────
function runSchema(PDO $pdo, string $rootDir): void
{
    $schemaFile = $rootDir . '/database/schema.sql';
    if (!file_exists($schemaFile)) {
        throw new RuntimeException("Schema file not found: {$schemaFile}");
    }

    $sql = file_get_contents($schemaFile);
    if ($sql === false) {
        throw new RuntimeException("Cannot read schema file: {$schemaFile}");
    }

    // Split on semicolons but handle DELIMITER blocks safely
    // Remove comments first
    $sql = preg_replace('/--[^\n]*\n/', "\n", $sql);
    $sql = preg_replace('/\/\*.*?\*\//s', '', $sql);

    // Split and execute
    $statements = array_filter(
        array_map('trim', explode(';', $sql)),
        static fn(string $s) => strlen($s) > 0
    );

    foreach ($statements as $statement) {
        if (trim($statement) === '') continue;
        try {
            $pdo->exec($statement);
        } catch (PDOException $e) {
            // Skip duplicate-definition errors (schema already partially applied)
            if (!in_array($e->getCode(), ['42S01', '42000'], true)) {
                // Only throw for non-duplicate-table errors
                if (str_contains($e->getMessage(), 'already exists')) continue;
                throw $e;
            }
        }
    }
}

